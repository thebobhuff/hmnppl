const DEFAULT_NOTIFICATIONS: Record<NotificationKey, boolean> = {
  newIncidentsReported: true,
  aiEvaluationComplete: true,
  documentApproved: true,
  meetingScheduled: true,
  documentAwaitingSignature: true,
  documentSigned: true,
  disputeSubmitted: true,
  aiBudgetAlerts: true,
  meetingReminder24h: true,
  signatureReminder72h: true,
};

const DEFAULT_COMPANY_FORM: CompanyFormState = {
  name: "",
  industry: "technology",
  size: "11-50",
  country: "US",
};

const DEFAULT_PROFILE_FORM: ProfileFormState = {
  firstName: "",
  lastName: "",
  phone: "",
  jobTitle: "",
  email: "",
  role: "",
};

const DEFAULT_SECURITY_FORM: SecurityFormState = {
  microsoftSso: false,
  googleSso: true,
  emailPassword: true,
};

const DEFAULT_AI_FORM: AIFormState = {
  confidenceThreshold: 90,
  autoGenerateDocuments: true,
  allowDisputes: true,
  aiMeetingSummaries: false,
  monthlyBudgetUsd: 50,
};

const TABS: { key: TabKey; label: string; icon: React.ReactNode }[] = [
  { key: "company", label: "Company", icon: <Building2 className="h-4 w-4" /> },
  { key: "profile", label: "Profile", icon: <User className="h-4 w-4" /> },
  { key: "notifications", label: "Notifications", icon: <Bell className="h-4 w-4" /> },
  { key: "ai", label: "AI Configuration", icon: <Brain className="h-4 w-4" /> },
];

export default function SettingsPage() {
  const setAuthUser = useAuthStore((state) => state.setUser);
  const { toast } = useToast();
  const [activeTab, setActiveTab] = useState<TabKey>("company");
  const [loading, setLoading] = useState(true);
  const [savingSection, setSavingSection] = useState<"company" | "profile" | "notifications" | "ai" | null>(null);
  const [role, setRole] = useState("");
  const [companyForm, setCompanyForm] = useState<CompanyFormState>(DEFAULT_COMPANY_FORM);
  const [profileForm, setProfileForm] = useState<ProfileFormState>(DEFAULT_PROFILE_FORM);
  const [notificationPrefs, setNotificationPrefs] =
    useState<Record<NotificationKey, boolean>>(DEFAULT_NOTIFICATIONS);
  const [securityForm, setSecurityForm] = useState<SecurityFormState>(DEFAULT_SECURITY_FORM);
  const [aiForm, setAiForm] = useState<AIFormState>(DEFAULT_AI_FORM);

  const breadcrumbs = usePageBreadcrumbs([
    { label: "Home", href: "/dashboard" },
    { label: "Settings" },
  ]);

  const isCompanyAdmin = role === "COMPANY_ADMIN" || role === "SUPER_ADMIN";

  useEffect(() => {
    let cancelled = false;

    async function loadSettings() {
      try {
        const [profileResponse, settingsResponse] = await Promise.all([
          usersAPI.me(),
          settingsAPI.get(),
        ]);

        if (cancelled) return;

        setRole(profileResponse.user.role);
        setProfileForm({
          firstName: profileResponse.user.firstName,
          lastName: profileResponse.user.lastName,
          phone: profileResponse.user.phone ?? "",
          jobTitle: profileResponse.user.jobTitle ?? "",
          email: profileResponse.user.email,
          role: profileResponse.user.role,
        });
        setCompanyForm(settingsResponse.company);
        setNotificationPrefs(settingsResponse.notifications as Record<NotificationKey, boolean>);
        setSecurityForm(settingsResponse.security);
        setAiForm(settingsResponse.ai);
      } catch (error) {
        if (cancelled) return;
        toast({
          title: "Failed to load settings",
          description:
            error instanceof APIError ? error.message : "Could not load settings right now.",
          variant: "error",
        });
      } finally {
        if (!cancelled) {
          setLoading(false);
        }
      }
    }

    loadSettings();

    return () => {
      cancelled = true;
    };
  }, [toast]);

  async function saveCompanySettings() {
    setSavingSection("company");
    try {
      const response = await settingsAPI.update({ company: companyForm });
      setCompanyForm(response.company);
      toast({ title: "Company settings saved", variant: "success" });
    } catch (error) {
      toast({
        title: "Failed to save company settings",
        description:
          error instanceof APIError ? error.message : "Please try again.",
        variant: "error",
      });
    } finally {
      setSavingSection(null);
    }
  }

  async function saveProfileSettings() {
    setSavingSection("profile");
    try {
      const response = await usersAPI.updateMe({
        firstName: profileForm.firstName,
        lastName: profileForm.lastName,
        phone: profileForm.phone,
        jobTitle: profileForm.jobTitle,
      });

      setProfileForm((current) => ({
        ...current,
        firstName: response.user.firstName,
        lastName: response.user.lastName,
        phone: response.user.phone ?? "",
        jobTitle: response.user.jobTitle ?? "",
      }));
      setAuthUser({
        id: response.user.id,
        email: response.user.email,
        firstName: response.user.firstName,
        lastName: response.user.lastName,
        role: response.user.role,
        avatarUrl: response.user.avatarUrl,
        tenantId: response.user.tenantId,
        tenantName: response.user.tenantName,
      });
      toast({ title: "Profile updated", variant: "success" });
    } catch (error) {
      toast({
        title: "Failed to update profile",
        description:
          error instanceof APIError ? error.message : "Please try again.",
        variant: "error",
      });
    } finally {
      setSavingSection(null);
    }
  }

  async function saveNotificationSettings() {
    setSavingSection("notifications");
    try {
      const response = await settingsAPI.update({ notifications: notificationPrefs });
      setNotificationPrefs(response.notifications as Record<NotificationKey, boolean>);
      toast({ title: "Notification preferences saved", variant: "success" });
    } catch (error) {
      toast({
        title: "Failed to save notifications",
        description:
          error instanceof APIError ? error.message : "Please try again.",
        variant: "error",
      });
    } finally {
      setSavingSection(null);
    }
  }

  async function saveAiSettings() {
    setSavingSection("ai");
    try {
      const response = await settingsAPI.update({
        security: securityForm,
        ai: aiForm,
      });
      setSecurityForm(response.security);
      setAiForm(response.ai);
      toast({ title: "AI settings saved", variant: "success" });
    } catch (error) {
      toast({
        title: "Failed to save AI settings",
        description:
          error instanceof APIError ? error.message : "Please try again.",
        variant: "error",
      });
    } finally {
      setSavingSection(null);
    }
  }

  if (loading) {
    return (
      <PageContainer
        title="Settings"
        description="Manage your account and platform configuration."
      >
        <div className="grid gap-6 lg:grid-cols-[200px_1fr]">
          <div className="space-y-2">
            {[1, 2, 3, 4].map((item) => (
              <Skeleton key={item} className="h-10 rounded-lg" />
            ))}
          </div>
          <Skeleton className="h-[520px] rounded-xl" />
        </div>
      </PageContainer>
    );
  }

  return (
    <PageContainer
      title="Settings"
      description="Manage your account and platform configuration."
    >
      <div className="grid gap-6 lg:grid-cols-[200px_1fr]">
        {/* Sidebar */}
        <nav className="flex flex-col gap-1">
          {TABS.map((tab) => (
            <button
              key={tab.key}
              onClick={() => setActiveTab(tab.key)}
              className={`flex items-center gap-2 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                activeTab === tab.key
                  ? "bg-brand-primary/10 text-brand-primary"
                  : "text-text-secondary hover:bg-brand-slate-light hover:text-text-primary"
              }`}
            >
              {tab.icon}
              {tab.label}
            </button>
          ))}
        </nav>

        {/* Content */}
        <div>
          {activeTab === "company" && (
            <CompanySettings
              canEdit={isCompanyAdmin}
              form={companyForm}
              security={securityForm}
              saving={savingSection === "company"}
              onChange={setCompanyForm}
              onSecurityChange={setSecurityForm}
              onSave={saveCompanySettings}
            />
          )}
          {activeTab === "profile" && (
            <ProfileSettings
              form={profileForm}
              saving={savingSection === "profile"}
              onChange={setProfileForm}
              onSave={saveProfileSettings}
            />
          )}
          {activeTab === "notifications" && (
            <NotificationSettings
              canEdit={isCompanyAdmin}
              preferences={notificationPrefs}
              saving={savingSection === "notifications"}
              onToggle={(key, checked) =>
                setNotificationPrefs((current) => ({ ...current, [key]: checked }))
              }
              onSave={saveNotificationSettings}
            />
          )}
          {activeTab === "ai" && (
            <AISettings
              canEdit={isCompanyAdmin}
              form={aiForm}
              saving={savingSection === "ai"}
              onChange={setAiForm}
              onSave={saveAiSettings}
            />
          )}
        </div>
      </div>
    </PageContainer>
  );
}

