function NotificationSettings({
  canEdit,
  preferences,
  saving,
  onToggle,
  onSave,
}: {
  canEdit: boolean;
  preferences: Record<NotificationKey, boolean>;
  saving: boolean;
  onToggle: (key: NotificationKey, checked: boolean) => void;
  onSave: () => void;
}) {
  return (
    <Card className="p-6">
      <h2 className="mb-4 flex items-center gap-2 font-display text-lg font-semibold text-text-primary">
        <Bell className="h-5 w-5" />
        Notification Preferences
      </h2>
      <div className="space-y-4">
        {NOTIFICATION_CONFIG.map((notification) => (
          <NotificationToggle
            key={notification.key}
            title={notification.title}
            description={notification.description}
            checked={preferences[notification.key]}
            disabled={!canEdit}
            onCheckedChange={(checked) => onToggle(notification.key, checked)}
          />
        ))}
      </div>
      {!canEdit && (
        <p className="mt-4 text-xs text-text-tertiary">
          Notification preferences are managed at the company level by administrators.
        </p>
      )}
      <div className="mt-6 flex justify-end">
        <Button onClick={onSave} disabled={!canEdit || saving}>
          <Save className="mr-1.5 h-4 w-4" />
          {saving ? "Saving..." : "Save Notifications"}
        </Button>
      </div>
    </Card>
  );
}

function NotificationToggle({
  title,
  description,
  checked,
  disabled,
  onCheckedChange,
}: {
  title: string;
  description: string;
  checked: boolean;
  disabled?: boolean;
  onCheckedChange: (checked: boolean) => void;
}) {
  return (
    <div className="flex items-center justify-between rounded-lg border border-border p-4">
      <div>
        <p className="text-sm font-medium text-text-primary">{title}</p>
        <p className="text-xs text-text-tertiary">{description}</p>
      </div>
      <Switch checked={checked} onCheckedChange={onCheckedChange} disabled={disabled} />
    </div>
  );
}

